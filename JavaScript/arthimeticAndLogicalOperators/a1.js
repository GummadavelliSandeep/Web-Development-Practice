let a = 2;
let b = "5";
let result = a + b;
// The above one performs the string concatenation
console.log(result);
let result1 = a - b;
// The above one performs the subtraction operation
console.log(result1);
let result2 = a * b;
console.log(result2);
let result3 = a / b;
console.log(result3);
// The above one performs the division of two operands(i,e number and string)

// Logical Operators
console.log(true && true); // true
console.log(true || false); // true
console.log(false && true); //false
console.log(false || false); //false
